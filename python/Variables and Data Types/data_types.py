# integer
age = 21
print(type(age))

# float
age = 21.5
print(type(age))

# complex
age = 8+9j
print(type(age))

# boolean
print(type(True))

# you can also convert data types
# range
print(list(range(0,10)))
print(list(range(0,10,2)))

# dictionaries
# key should be unique
dict = {'name':'Mofiz','location':'Bangladesh',}
print(dict)
print(dict.keys())
print(dict['name'])