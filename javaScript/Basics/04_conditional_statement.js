
// Even or Odd
var num1 = 10;
var num2 = -13;

if(num1 % 2 == 0){
    console.log(num1, "is Even");
}
else{
    console.log(num1, "is Odd");
}

// positive or negative

if(num2 > 0){
    console.log(num2 ,'is positive');
}
else if(num2 < 0){
    console.log(num2, 'is negative');
}

else{
    console.log(num2, 'is Zero');
}
